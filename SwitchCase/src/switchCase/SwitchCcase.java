package switchCase;

public class SwitchCcase {

	public static void main(String[] args) {
		int dayOfWeek = 2;
		switch(dayOfWeek) {
		case 1:
		System.out.println("im on leave");	
		break;
 		case 2:
			System.out.println("i have to work hark so that i will be financial stable at the age 24");
	  		break;
		case 3:
			System.out.println("its time for cricket");
			break;
			default: 
			    System.out.println("lets study");
		}

	}

}
